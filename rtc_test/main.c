/* Private define ------------------------------------------------------------*/
/* USER CODE BEGIN PD */
HAL_StatusTypeDef DS3231_GetTime(I2C_HandleTypeDef *hi2c, DS3231_Time *time) {
    uint8_t buffer[3];
    HAL_StatusTypeDef status = HAL_I2C_Mem_Read(hi2c, DS3231_ADDRESS, 0x00, I2C_MEMADD_SIZE_8BIT, buffer, 3, 100);

    if (status == HAL_OK) {
        time->seconds = (buffer[0] >> 4) * 10 + (buffer[0] & 0x0F);
        time->minutes = (buffer[1] >> 4) * 10 + (buffer[1] & 0x0F);
        time->hours = ((buffer[2] & 0x30) >> 4) * 10 + (buffer[2] & 0x0F);
    }
    return status;
}

